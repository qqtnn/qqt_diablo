        if last_waypoint_index > 5 then
            angle = waypoint_table[last_waypoint_index - 2]:get_angle(waypoint_table[last_waypoint_index], _waypoint)
        end

        if angle < 90 or i < 15 then
            local score = distance_sq + angle
