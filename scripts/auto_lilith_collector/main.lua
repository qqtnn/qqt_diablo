                best_score = score
            end
        end

        ::continue::
    end

    if not best_index then
        -- console.print("No valid next waypoint found.")
        return nil
    end

    last_waypoint_index = best_index

    local next_waypoint = utility.set_height_of_valid_position(waypoint_table[best_index])
